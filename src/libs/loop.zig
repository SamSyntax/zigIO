// ╔══════════════════════════════════════ CORE ══════════════════════════════════════╗

    pub inline fn untilWith
    ( _cond: anytype, _call: anytype, _args: anytype )
    !void
    {
        while (true)
        {
            try @call(.auto, _call, _args);

            if (!try _cond()) break;
        }
    }

